package Entity;

public class branch {
	private int id;
	private String bhname;
	private int bhrenshu;
	private String bhphone;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getBhname() {
		return bhname;
	}

	public void setBhname(String bhname) {
		this.bhname = bhname;
	}

	public int getBhrenshu() {
		return bhrenshu;
	}

	public void setBhrenshu(int bhrenshu) {
		this.bhrenshu = bhrenshu;
	}

	public String getBhphone() {
		return bhphone;
	}

	public void setBhphone(String bhphone) {
		this.bhphone = bhphone;
	}

}
